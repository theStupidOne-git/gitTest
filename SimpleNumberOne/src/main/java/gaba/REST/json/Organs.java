package gaba.REST.json;

public class Organs {
	
private String Liver;
private String Kidneys;
private String Hearth;

public Organs() {}

public String getLiver() {
	return Liver;
}

public void setLiver(String liver) {
	Liver = liver;
}

public String getKidneys() {
	return Kidneys;
}

public void setKidneys(String kidneys) {
	Kidneys = kidneys;
}

public String getHearth() {
	return Hearth;
}

public void setHearth(String hearth) {
	Hearth = hearth;
}


}
